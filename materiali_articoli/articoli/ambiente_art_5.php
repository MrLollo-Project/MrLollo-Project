                            2) Bonifica:<br>
                            Le piante di canapa possono essere utilizzate per la bonifica di terreni contaminati da metalli
                            pesanti attraverso un processo chiamato phytoremediation.
                            La canapa è considerata particolarmente adatta allo scopo, in quanto, attraverso il proprio
